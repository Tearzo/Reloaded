#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_print_alphabet(void)
{
	char	c;

	c = 96;
	while (c < 122)
	{
		c++;
		ft_putchar(c);
	}
}
